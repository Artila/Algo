/**
 * 传入二进制字符串，翻译成英语句子并返回。
 * 二进制字符串是以空格分隔的。
 * 这题是对前面的复习。
 */

function binaryAgent(str) {
  var arr = str.split(" ").map(function(val) {
    // 二进制转十进制，再转字母
    return String.fromCharCode(parseInt(val, 2));
  }).join("");

  console.log(arr);
  return arr;
}

